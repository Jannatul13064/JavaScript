//Switch Structure
//Switch is faster than if else;
var color = '';
switch (color) {
    case 'blue':
        console.log('Color is blue');
        break;
    case 'red':
        console.log('Color is red');
        break;
    case 'yellow':
        console.log('Color is yellow');
        break;
    case 'white':
        console.log('Color is white');
        break;
    case 'pink':
        console.log('Color is pink');
        break;
    default:// look like else condition
        console.log('The default color is black');
}